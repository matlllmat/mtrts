-- ------------------------------------------------------------
-- TABLE 1: asset_categories
-- Dynamic — IT staff can add and edit categories.
-- Referenced by: assets.category_id
-- ------------------------------------------------------------
CREATE TABLE asset_categories (
  category_id     INT           PRIMARY KEY AUTO_INCREMENT,
  category_name   VARCHAR(100)  NOT NULL UNIQUE,
    -- e.g. 'Projector', 'Sound System', 'AV Switcher',
    --      'Display', 'Microphone', 'AV Rack'
  has_bulb_hours  TINYINT(1)    NOT NULL DEFAULT 0,
    -- 1 = this category tracks bulb hours (e.g. Projectors)
    -- 0 = bulb hours not applicable
  description     VARCHAR(255)  NULL,
  created_at      DATETIME      NOT NULL DEFAULT CURRENT_TIMESTAMP,
  updated_at      DATETIME      NOT NULL DEFAULT CURRENT_TIMESTAMP
                                ON UPDATE CURRENT_TIMESTAMP
);

