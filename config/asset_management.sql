-- ------------------------------------------------------------
-- TABLE 2: locations
-- One row per unique room in the institution.
-- Combination of building + floor + room must be unique.
-- Referenced by: assets.location_id
-- ------------------------------------------------------------
CREATE TABLE locations (
  location_id   INT           PRIMARY KEY AUTO_INCREMENT,
  building      VARCHAR(100)  NOT NULL,
    -- e.g. 'Main Building', 'Science Hall', 'Annex'
  floor         VARCHAR(50)   NOT NULL,
    -- e.g. 'Ground Floor', '1st Floor', '2nd Floor'
  room          VARCHAR(100)  NOT NULL,
    -- e.g. 'Room 101', 'Media Lab A', 'Auditorium'
  created_at    DATETIME      NOT NULL DEFAULT CURRENT_TIMESTAMP,

  UNIQUE (building, floor, room)
    -- Prevents duplicate location entries.
    -- Same room cannot be registered twice.
);

